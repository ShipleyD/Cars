    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        //////////////////////////////////////////////
        //Check Colour is selected
        //////////////////////////////////////////////
        if (!rdobtnBlack.isSelected() && !rdobtnBlue.isSelected() && !rdobtnRed.isSelected() && !rdobtnWhite.isSelected()){
            JOptionPane.showMessageDialog(rootPane, "Please select a colour.");
            return;
        }
        //////////////////////////////////////
        //Car Selecter
        //////////////////////////////////////
        String chosenCar = comboCarType.getSelectedItem().toString();
        if (chosenCar.equals("Civic")){
            car = new Civic();
        }
        if (chosenCar.equals("Jazz")){
            car = new Jazz();
        }
        if (chosenCar.equals("HR-V")){
            car = new HRV();
        }
        if (chosenCar.equals("CR-V")){
            car = new CRV();
        }
        //////////////////////////////////////////////////////////
        //Colour Selecter
        //////////////////////////////////////////////////////////
        if (rdobtnBlack.isSelected()){
            colour = "Black";
        }
        if (rdobtnBlue.isSelected()){
            colour = "Blue";
        }
        if (rdobtnRed.isSelected()){
            colour = "Red";
        }
        if (rdobtnWhite.isSelected()){
            colour = "White";
        }
        ///////////////////////////////////////////////////////////////////
        //Interior add-ons
        ///////////////////////////////////////////////////////////////////
        if (chkboxBluetooth.isSelected()){
            car = new Bluetooth(car);
        }
        if (chkboxHetaedSeats.isSelected()){
            car = new HeatedSeats(car);
        }
        if (chkboxSatNav.isSelected()){
            car = new SatNav(car);
        }
        if (chkboxLeather.isSelected()){
            car = new Leather(car);
        }
        ///////////////////////////////////////////////////////////////
        //exterior add-ons
        ///////////////////////////////////////////////////////////////
        if (chkboxAlloys.isSelected()){
            car = new Alloys(car);
        }
        if (chkboxMetalic.isSelected()){
            car = new Metalic(car);
        }
        if (chkboxSunroof.isSelected()){
            car = new Sunroof(car);
        }
        ///////////////////////////////////////////////////////////////////
        //Get The price into standard money form
        ///////////////////////////////////////////////////////////////////
        price = roundIt(car.cost());
        ///////////////////////////////////////////////////////////////////
        //Set the label
        ///////////////////////////////////////////////////////////////////
        carType = car.getDescription() + " in " + car.getColour(colour);
        String labelText = car.getDescription() + " in " + car.getColour(colour) + ". Total Cost = Â£" + price;
        lblSelectedCar.setText("<html>" + labelText + "</html>");
        btnForm.setEnabled(true);
        
    }//GEN-LAST:event_btnConfirmActionPerformed

