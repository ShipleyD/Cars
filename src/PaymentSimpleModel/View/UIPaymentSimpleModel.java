    private void btnPayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPayActionPerformed
        // TODO add your handling code here:
        if (!rdoCashPayment.isSelected()&&!rdoCardPayment.isSelected()&&!rdoCreditPayment.isSelected()){
            return;
        } else {
            try {
                // send the payment to the  ModelController (see method below)
                sendToModel();
            } catch (SQLException e) {
                System.err.println("Grr - Cannot connect the database!" + e);
            }
        JOptionPane.showMessageDialog(rootPane, "Payment Complete.");
        otherForm.setEnabled(true);
        otherForm.cleanPage();
        this.dispose();
        }
    }//GEN-LAST:event_btnPayActionPerformed


    private void sendToModel()throws SQLException
    { 
        //Create a new connection to the database
        connectionToDatabase = new ConnectionToDatabase(this.aCarType, getCarPrice, paymentPlan, paymentMethod); 
        connectionToDatabase.addNewRecord();
    }
    

private double roundIt(double d)
        {
            // turn our double value into a BigDecimal variable
            // by passing it to the constructor of our new BigDecimal
            // object named bd
            BigDecimal bd = new BigDecimal(d);
            // now round it up to 2 dec places
            // If we were crooked we could round down - but we'll round up
            BigDecimal rounded = bd.setScale(2, RoundingMode.HALF_UP);
            // now turn the rounded value back to a double
            d = rounded.doubleValue();
            
            // now give it back to the sendToModel method 
            return d;
        }


