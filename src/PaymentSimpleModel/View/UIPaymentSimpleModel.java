    /**
     * 
     * @param ap
     * gets passed a new object of type CardPayment, CreditPayment, etc
     * from the radio button methods directly above
     */
    private void createPaymentType(AbstractPayment ap)
    {
        
        // get the payment value from the text box
        getPayment = lblgetPayment.getText();
        

        // get the payment value from the textBox and put it into the variable
        // paid
        // the text box gives us a String variable so we need to change it to a
        // double variable so that we can do some Maths on it if we want to
        paid = Double.parseDouble(getPayment);
       
        // this object just got passed to us - this is the payment type
        // i.e. Card Payment, etc 
        payment = ap;
   
        // check the payment type
        String OType = "" + payment.getClass().getSimpleName();
        
        // now set the payment in the object by calling the setThePayment
        // method and giving it the value from the text box vhanged to a double
        payment.setThePayment(paid);
        
        // keep a record of the current object i.e. CardPayment, CreditPayment
        currentPayment = ap; 
        
        
        // print dtails to the screen
        lblShowDescription.setText("Type of Payment : " + payment.getDescription());
        lblShowPaid.setText("Total Cost : £ " + payment.cost());
    }
    

